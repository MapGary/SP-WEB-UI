package utils;

public class SecurityUtils {

    /**
     * Классический проверочный SQL-инъекционный payload.
     * Не агрессивен, часто используется в тестах как сигнатура возможной уязвимости.
     * Объяснение: если сервер неправильно подставляет строку в SQL-запрос,
     * то выражение "' OR '1'='1" может сделать условие всегда истинным.
     */
    public static String sqlInjectionPayload() {
        return "' OR '1'='1";
    }
}
